package com.xw.milk.model;

import java.io.Serializable;
import java.math.BigDecimal;
import javax.persistence.*;

@Table(name = "biz_product_t")
public class BizProduct implements Serializable{
    @Id
    private Integer id;

    /**
     * 产品编号
     */
    @Column(name = "product_num")
    private Integer productNum;

    /**
     * 产品名称
     */
    @Column(name = "product_name")
    private String productName;

    /**
     * 产品规格
     */
    @Column(name = "product_size")
    private Double productSize;

    /**
     * 产品品牌（如：蒙牛）
     */
    @Column(name = "brand_id")
    private Integer brandId;

    /**
     * 产品一级分类（如：乳品、饮料）
     */
    @Column(name = "parent_id")
    private Integer parentId;

    /**
     * 产品二级分类
     */
    @Column(name = "second_class_id")
    private Integer secondClassId;

    /**
     * 产品价格
     */
    @Column(name = "product_price")
    private BigDecimal productPrice;

    /**
     * 打折
     */
    @Column(name = "priduct_discount")
    private Integer priductDiscount;

    /**
     * 产品介绍
     */
    @Column(name = "product_intro")
    private String productIntro;

    /**
     * @return id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * 获取产品编号
     *
     * @return product_num - 产品编号
     */
    public Integer getProductNum() {
        return productNum;
    }

    /**
     * 设置产品编号
     *
     * @param productNum 产品编号
     */
    public void setProductNum(Integer productNum) {
        this.productNum = productNum;
    }

    /**
     * 获取产品名称
     *
     * @return product_name - 产品名称
     */
    public String getProductName() {
        return productName;
    }

    /**
     * 设置产品名称
     *
     * @param productName 产品名称
     */
    public void setProductName(String productName) {
        this.productName = productName;
    }

    /**
     * 获取产品规格
     *
     * @return product_size - 产品规格
     */
    public Double getProductSize() {
        return productSize;
    }

    /**
     * 设置产品规格
     *
     * @param productSize 产品规格
     */
    public void setProductSize(Double productSize) {
        this.productSize = productSize;
    }

    /**
     * 获取产品品牌（如：蒙牛）
     *
     * @return brand_id - 产品品牌（如：蒙牛）
     */
    public Integer getBrandId() {
        return brandId;
    }

    /**
     * 设置产品品牌（如：蒙牛）
     *
     * @param brandId 产品品牌（如：蒙牛）
     */
    public void setBrandId(Integer brandId) {
        this.brandId = brandId;
    }

    /**
     * 获取产品一级分类（如：乳品、饮料）
     *
     * @return parent_id - 产品一级分类（如：乳品、饮料）
     */
    public Integer getParentId() {
        return parentId;
    }

    /**
     * 设置产品一级分类（如：乳品、饮料）
     *
     * @param parentId 产品一级分类（如：乳品、饮料）
     */
    public void setParentId(Integer parentId) {
        this.parentId = parentId;
    }

    /**
     * 获取产品二级分类
     *
     * @return second_class_id - 产品二级分类
     */
    public Integer getSecondClassId() {
        return secondClassId;
    }

    /**
     * 设置产品二级分类
     *
     * @param secondClassId 产品二级分类
     */
    public void setSecondClassId(Integer secondClassId) {
        this.secondClassId = secondClassId;
    }

    /**
     * 获取产品价格
     *
     * @return product_price - 产品价格
     */
    public BigDecimal getProductPrice() {
        return productPrice;
    }

    /**
     * 设置产品价格
     *
     * @param productPrice 产品价格
     */
    public void setProductPrice(BigDecimal productPrice) {
        this.productPrice = productPrice;
    }

    /**
     * 获取打折
     *
     * @return priduct_discount - 打折
     */
    public Integer getPriductDiscount() {
        return priductDiscount;
    }

    /**
     * 设置打折
     *
     * @param priductDiscount 打折
     */
    public void setPriductDiscount(Integer priductDiscount) {
        this.priductDiscount = priductDiscount;
    }

    /**
     * 获取产品介绍
     *
     * @return product_intro - 产品介绍
     */
    public String getProductIntro() {
        return productIntro;
    }

    /**
     * 设置产品介绍
     *
     * @param productIntro 产品介绍
     */
    public void setProductIntro(String productIntro) {
        this.productIntro = productIntro;
    }
}